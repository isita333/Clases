def Sumar (a, b):
    return a + b
def Restar (c, d):
    return c - d
def Dividir (e, f):
    return e / f


Resultado1 = Sumar(a=10, b=15)
Resultado2 = Restar(c=5, d=2 )
Resultado3 = Dividir(e=8, f=6)

print(f"El resultado de la suma es: {Resultado1}")
print(f"El resultado de la resta es: {Resultado2}")
print(f"El resultado de la division es: {Resultado3}")

